SELECT s.P_CODE, s.CRUISE_CODE,s.SAMPLE_ID, s.A_SECTOR, s.SHELF, s.REEF_ID, s.REEF_NAME,s.AIMS_REEF_NAME,s.GBRMPA_REEF_ID,
s.REEF_LAT, s.REEF_LONG, s.OPENORCLOSED, s.OPENORCLOSED_AFTER2004,
s.REPORT_YEAR, s.VISIT_NO,s.SAMPLE_DATE,m.TOW_SEQ_NO,
m.LIVE_CORAL, m.DEAD_CORAL,m.COT_COUNT, m.SCAR,m.BLEACHING_PERHC
FROM V_RM_SAMPLE s INNER JOIN RM_MANTA m ON s.SAMPLE_ID = m.SAMPLE_ID
where (s.sample_class in ('K', 'C', 'G') or s.sample_class is null) and s.REPORT_YEAR>1985
and p_code in ('IN', 'RM', 'RMRAP', 'RAP') order by s.report_year, s.reef_id
